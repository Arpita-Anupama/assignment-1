#list
fruits = ['apple', 'banana', 'cherry']
fruits.append("orange")

#append
a = ["apple", "banana", "cherry"]
b = ["Ford", "BMW", "Volvo"]
a.append(b)

#clear
fruits = ['apple', 'banana', 'cherry', 'orange']
fruits.clear()

#copy
fruits = ['apple', 'banana', 'cherry', 'orange']
x = fruits.copy()

#count
fruits = ['apple', 'banana', 'cherry']
x = fruits.count("cherry")

#extend
fruits = ['apple', 'banana', 'cherry']
cars = ['Ford', 'BMW', 'Volvo']
fruits.extend(cars)

#index
fruits = ['apple', 'banana', 'cherry']
x = fruits.index("cherry")

#insert
fruits = ['apple', 'banana', 'cherry']
fruits.insert(1, "orange")

#pop
fruits = ['apple', 'banana', 'cherry']
fruits.pop(1)

#remove
fruits = ['apple', 'banana', 'cherry']
fruits.remove("banana")

#reverse
fruits = ['apple', 'banana', 'cherry']
fruits.reverse()

#sort
cars = ['Ford', 'BMW', 'Volvo']
cars.sort()
